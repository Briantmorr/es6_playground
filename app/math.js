export const add = (x, y) => x + y;
export function  subtraction(x, y){
    return x - y;
}

export function  multiplication(x, y){
    return x * y
}

export function  division(x, y){
    return x / y
}

export function  modulus(x, y){
    return x % y
}

export default () => console.log('this is the default export')
